    private void btnAddCandidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCandidateActionPerformed
        // TODO add your handling code here:
        if(!(txtCandidate.getText()).isEmpty()){
            Candidate.addElement(txtCandidate.getText());
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Candidate not entered.");
        }
    }//GEN-LAST:event_btnAddCandidateActionPerformed

    private void btnRemoveCandidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveCandidateActionPerformed
        // TODO add your handling code here:
        if(!(txtCandidate.getText()).isEmpty()){
            Candidate.removeElement(txtCandidate.getText());
            txtCandidate.setText("");
        }
        else if(!((String) listCandidates.getSelectedValue()).isEmpty()){
            Candidate.removeElement(listCandidates.getSelectedValue());
        }
        else{
            JOptionPane.showMessageDialog(this,"Candidate not specified");
        }
    }//GEN-LAST:event_btnRemoveCandidateActionPerformed

